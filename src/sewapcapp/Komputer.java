    private void bEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditActionPerformed
        // TODO add your handling code here:
        try {
            String sql ="UPDATE tb_komputer SET Nama = '"+eNama.getText()+"', CPU = '"+eCPU.getText()+"', GPU = '"+eGPU.getText()+"', RAM = '"+eRAM.getText()+"' WHERE Nama = '"+eNama.getText()+"'";
            pst = cn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data komputer berhasil di edit");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Perubahan Data User Gagal"+e.getMessage());
        }
        load_table();
        kosong();
    }//GEN-LAST:event_bEditActionPerformed

