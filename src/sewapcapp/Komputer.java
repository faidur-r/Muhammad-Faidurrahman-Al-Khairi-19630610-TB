            String sql = "INSERT INTO tb_komputer (Nama, CPU, GPU, RAM) VALUES ('"+eNama.getText()+"','"+eCPU.getText()+"','"+eGPU.getText()+"','"+eRAM.getText()+"')";
            st = cn.createStatement();
