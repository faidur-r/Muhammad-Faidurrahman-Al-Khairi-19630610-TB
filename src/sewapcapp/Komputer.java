        eNama.setText(nama);
        String cpu = tKomputer.getValueAt(baris,1).toString();
        eCPU.setText(cpu);
        String gpu =tKomputer.getValueAt(baris, 2).toString();
        eGPU.setText(gpu);
        String ram = tKomputer.getValueAt(baris,3).toString();
