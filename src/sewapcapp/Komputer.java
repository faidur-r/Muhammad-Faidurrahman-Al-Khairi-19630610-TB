    private void load_table(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama");
        model.addColumn("CPU");
        model.addColumn("GPU");
        model.addColumn("RAM");
        
        try {
            String sql = "select * from tb_komputer";
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            while(rs.next()){
                model.addRow(new Object[]{rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)});
            }
            tKomputer.setModel(model);
            tKomputer.getColumnModel().getColumn(0).setPreferredWidth(15);
            tKomputer.getColumnModel().getColumn(3).setPreferredWidth(15);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
